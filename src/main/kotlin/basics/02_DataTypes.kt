package basics

/*
To create a variable, use var or val,
and assign a value to it with the equal sign (=):
 */
fun main(){
    //var
    var x = 15
    x=16
    println(x)

    //val
    val a = 12
    // a = 13   error
    println(a)

    //Float
    var b:Float = 14.12f
    b = 12.4535f

    //Double
    val d:Double = 20.15

    //Boolean
    val n:Boolean = true

    //Char
    val ch:Char = 'a'

    //String
    val s:String = "String"
}