//String Tempaletes / Interpolation
/*
Instead of concatenation, you can also use "string templates",
which is an easy way to add variables and expressions inside a string.
Just refer to the variable with the $ symbol:
 */
//String Tempaletes / Interpolation
fun main(){
    val name = "Oybek"
    val surname = "Kholikov"
    val age = 29

    println("My  fullname is $name $surname and age is $age ")

}
//output
//My name fullname Oybek Kholikov and age is 29
